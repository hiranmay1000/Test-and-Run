#include <iostream>
#include <vector>
using namespace std;

int binarySearch(int arr[], int size, int key)
{
    int start = 0;
    int end = size - 1;
    int mid = (start + end) / 2;

    while (start <= end)
    {
        if (arr[mid] == key)
        {
            return mid;
        }
        else if (arr[mid] < key)
        {
            start = mid + 1;
        }
        else
        {
            end = mid - 1;
        }
        mid = (start + end) / 2;
    }
    return -1;
}

int main()
{
    int arr[5] = {2, 4, 6, 8, 12};

    int ans = binarySearch(arr, 5, 6);
    cout << "Index -> " << ans;

    return 0;
}